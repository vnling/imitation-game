//generate charRNN data
function generate() {
  //inputs
  const data = {
    seed: textInput,
    temperature: 0.5,
    length: 300
  };

  // Generate text with the charRNN
  charRNN.generate(data, gotData);

  // When it's done
  function gotData(err, result) {
    console.log(textInput + result.sample);
    //directly set a sample to change 
    //TODO: any way to make it same time as other two?
    samplesArray[2].innerText = (textInput + result.sample);
  }
}

//sends info to runway GPT2 model
async function sendVector(humanText, charRNNText) {
  //model input
  const data = {
    prompt: textInput
  }

  //information sent to model
  const info = {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify(data),
  };

  const response = await fetch("/runwayml", info);
  const outputs = await response.json();
  const script = outputs.generated_text;
  //display GPT2 and human text as samples
  samplesArray[0].innerHTML = script;
  samplesArray[1].innerHTML = humanText;
  
}